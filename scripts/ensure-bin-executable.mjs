#!/usr/bin/env node

import { chmodSync, existsSync, statSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const distServerPath = resolve(process.cwd(), "dist/server.js");
const launcherPath = resolve(process.cwd(), "bin/agent-mcp");

const targetPaths = (
  process.argv.length > 2
    ? process.argv.slice(2)
    : [distServerPath, launcherPath]
).map((target) => resolve(process.cwd(), target));

function shellQuote(value) {
  return `'${value.replaceAll("'", `'\"'\"'`)}'`;
}

function detectPreferredRuntime() {
  if (process.versions.bun) {
    return {
      runtime: "bun",
      absolutePath: process.execPath,
      fallbackOrder: ["bun", "node"],
    };
  }

  const userAgent = (process.env.npm_config_user_agent ?? "").toLowerCase();
  const execPath = (process.env.npm_execpath ?? "").toLowerCase();

  if (userAgent.startsWith("bun/") || execPath.includes("/bun")) {
    return {
      runtime: "bun",
      absolutePath: process.env.npm_execpath ?? "bun",
      fallbackOrder: ["bun", "node"],
    };
  }

  return {
    runtime: "node",
    absolutePath: process.execPath,
    fallbackOrder: ["node", "bun"],
  };
}

function writeLauncherScript() {
  const { runtime, absolutePath, fallbackOrder } = detectPreferredRuntime();
  const fallbackChecks = fallbackOrder
    .map(
      (candidate, index) =>
        `${index === 0 ? "if" : "elif"} command -v ${candidate} >/dev/null 2>&1; then exec ${candidate} "$SERVER_JS" "$@"`,
    )
    .join("; ");
  const preferredPathCheck = `[ -x ${shellQuote(absolutePath)} ] && exec ${shellQuote(absolutePath)} "$SERVER_JS" "$@"`;

  const launcherContents = `#!/bin/sh
# generated by scripts/ensure-bin-executable.mjs
SCRIPT_DIR="$(CDPATH= cd -- "$(dirname "$0")" && pwd)"
SERVER_JS="$SCRIPT_DIR/../dist/server.js"

${preferredPathCheck}
${fallbackChecks}
else
  echo "agent-mcp requires ${fallbackOrder.join(" or ")} in PATH" >&2
  exit 127
fi
`;

  writeFileSync(launcherPath, launcherContents);
}

writeLauncherScript();

for (const targetPath of targetPaths) {
  if (!existsSync(targetPath)) {
    throw new Error(
      `Cannot mark executable: file does not exist: ${targetPath}`,
    );
  }

  if (process.platform !== "win32") {
    const currentMode = statSync(targetPath).mode;
    chmodSync(targetPath, currentMode | 0o111);
  }
}
